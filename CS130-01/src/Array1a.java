
public class Array1a {

	public static void main(String[] args) {
		int Multiple = 10;
	      int[] list = new int[10];
	      
	      //  Initialize the array values
	      for (int i= 0; i < 10; i++)
	         list[i] = i * Multiple; // Result need to store in the array
	  
	            
	      //  Print the array values
	      for (int value : list)
	         System.out.print(value + "  ");


	}

}
